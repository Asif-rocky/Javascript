// 1. Loigcal AND (&&)
const a = true;
const b = false;
const c = 4;

console.log(a && b); // false
console.log(c > 2 && c < 2); // false

// 2. Logical OR (||)
console.log(a || b); // true
console.log(c > 2 || c < 2); // true

// 3. Logical NOT(!)
console.log(!a); // false
console.log(!b); // true
